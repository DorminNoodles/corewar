#include "corewar.h"

int  reverse_int(int nb)
{
return (nb & 0x000000FFU) << 24 | (nb & 0x0000FF00U) << 8 |
       (nb & 0x00FF0000U) >> 8 | (nb & 0xFF000000U) >> 24;
}

short reverse_short(short nb)
{
    return ((nb & 0xFFU) << 8 | (nb & 0xFF00U) >> 8);
}
