#include "corewar.h"

void	show_mem(t_vm *vm)
{
	int i;
	i = 0;

	while (i < MEM_SIZE)
	{
		printf("%02x ", *((unsigned char*)vm->mem + i));
		if (i % 82 == 0 && i > 0)
			printf("\n");
		i++;
	}
}
